import sys

# 입력을 한 줄씩 정수형으로 받습니다.
# A: (1)에 들어갈 세 자리 수 (예: 472)
# B: (2)에 들어갈 세 자리 수 (예: 385)
A = int(sys.stdin.readline())
B = int(sys.stdin.readline())

# B를 각 자릿수로 분리합니다.
# B_ones: B의 일의 자리 수 (예: 385 % 10 = 5)
B_ones = B % 10
# B_tens: B의 십의 자리 수 (예: (385 // 10) % 10 = 8)
B_tens = (B // 10) % 10
# B_hundreds: B의 백의 자리 수 (예: 385 // 100 = 3)
B_hundreds = B // 100

# 1. (3) 값 계산: A * (B의 일의 자리 수)
# 예시: 472 * 5 = 2360
step3 = A * B_ones
print(step3)

# 2. (4) 값 계산: A * (B의 십의 자리 수)
# 이 값은 사실 A * (B의 십의 자리 수 * 10) 이지만,
# 문제의 그림 (4) 라인에는 자리 맞춤을 한 472 * 8 = 3776만 표시됩니다.
# 예시: 472 * 8 = 3776
step4 = A * B_tens
print(step4)

# 3. (5) 값 계산: A * (B의 백의 자리 수)
# 이 값은 사실 A * (B의 백의 자리 수 * 100) 이지만,
# 문제의 그림 (5) 라인에는 자리 맞춤을 한 472 * 3 = 1416만 표시됩니다.
# 예시: 472 * 3 = 1416
step5 = A * B_hundreds
print(step5)

# 4. (6) 값 계산: 최종 곱셈 결과 A * B
# 예시: 472 * 385 = 181720
step6 = A * B
print(step6)